    /**
     * 记录线程池状态和线程池中线程个数（使用一个变量保存两种信息）
     * 高 3 位表示线程状态
     * 低 29 位表示线程的个数
     * 默认 RUNNING 状态，线程个数为 0
     */
    private final AtomicInteger ctl = new AtomicInteger(ctlOf(RUNNING, 0));

    /**
     * 线程个数掩码位数，并不是所有平台的 int 类型都是 32 位的，所以准确的说，
     * 是具体平台下 Integer 的二进制位数减去 3 后的剩余位数用于表示线程的个数。
     */
    private static final int COUNT_BITS = Integer.SIZE - 3;

    /**
     * 线程的最大个数：低 29 位全为 1，即：0001 1111 1111 1111 1111 1111 1111 1111
     */
    private static final int CAPACITY = (1 << COUNT_BITS) - 1;

    // 线程池状态（高 3 位）

    /**
     * 1110 0000 0000 0000 0000 0000 0000 0000
     * 高三位：111
     * int 数值：-536870912
     */
    private static final int RUNNING = -1 << COUNT_BITS;

    /**
     * 0000 0000 0000 0000 0000 0000 0000 0000
     * 高三位：000
     * int 数值：0
     */
    private static final int SHUTDOWN = 0 << COUNT_BITS;

    /**
     * 0010 0000 0000 0000 0000 0000 0000 0000
     * 高三位：001
     * int 数值：536870912
     */
    private static final int STOP = 1 << COUNT_BITS;

    /**
     * 0100 0000 0000 0000 0000 0000 0000 0000
     * 高三位：010
     * int 数值：1073741824
     */
    private static final int TIDYING = 2 << COUNT_BITS;

    /**
     * 0110 0000 0000 0000 0000 0000 0000 0000
     * 高三位：011
     * int 数值：1610612736
     */
    private static final int TERMINATED = 3 << COUNT_BITS;

    /**
     * 获取高 3 位（线程池状态）
     *
     * @param c ctl
     * @return 线程池状态
     */
    private static int runStateOf(int c) {
        return c & ~CAPACITY;
    }

    /**
     * 获取低 29 位（线程池的线程个数）
     *
     * @param c ctl
     * @return 线程池的线程个数
     */
    private static int workerCountOf(int c) {
        return c & CAPACITY;
    }

    /**
     * 根据 rs 和 wc 计算 ctl 值
     *
     * @param rs 线程池状态
     * @param wc 线程池的线程个数
     * @return ctl 值
     */
    private static int ctlOf(int rs, int wc) {
        return rs | wc;
    }

    /**
     * 状态 c 是否小于状态 s
     *
     * @param c 线程池状态 c
     * @param s 线程池状态 s
     * @return c < s
     */
    private static boolean runStateLessThan(int c, int s) {
        return c < s;
    }

    /**
     * 状态 c 是否大于等于状态 s
     *
     * @param c 线程池状态 c
     * @param s 线程池状态 s
     * @return c >= s
     */
    private static boolean runStateAtLeast(int c, int s) {
        return c >= s;
    }

    /**
     * 线程池状态 c 是否为 RUNNING（小于 SHUTDOWN 即是 RUNNING）
     *
     * @param c 线程池状态 c
     * @return c 是否等于 RUNNING
     */
    private static boolean isRunning(int c) {
        return c < SHUTDOWN;
    }

    /**
     * Attempts to CAS-increment the workerCount field of ctl.
     */
    private boolean compareAndIncrementWorkerCount(int expect) {
        return ctl.compareAndSet(expect, expect + 1);
    }

    /**
     * Attempts to CAS-decrement the workerCount field of ctl.
     */
    private boolean compareAndDecrementWorkerCount(int expect) {
        return ctl.compareAndSet(expect, expect - 1);
    }

    /**
     * Decrements the workerCount field of ctl. This is called only on
     * abrupt termination of a thread (see processWorkerExit). Other
     * decrements are performed within getTask.
     */
    private void decrementWorkerCount() {
        do {
        } while (!compareAndDecrementWorkerCount(ctl.get()));
    }

    /**
     * 用于存放任务的任务队列
     */
    private final BlockingQueue<Runnable> workQueue;

    /**
     * Lock held on access to workers set and related bookkeeping.
     * While we could use a concurrent set of some sort, it turns out
     * to be generally preferable to use a lock. Among the reasons is
     * that this serializes interruptIdleWorkers, which avoids
     * unnecessary interrupt storms, especially during shutdown.
     * Otherwise exiting threads would concurrently interrupt those
     * that have not yet interrupted. It also simplifies some of the
     * associated statistics bookkeeping of largestPoolSize etc. We
     * also hold mainLock on shutdown and shutdownNow, for the sake of
     * ensuring workers set is stable while separately checking
     * permission to interrupt and actually interrupting.
     */
    private final ReentrantLock mainLock = new ReentrantLock();

    /**
     * Set containing all worker threads in pool. Accessed only when
     * holding mainLock.
     */
    private final HashSet<Worker> workers = new HashSet<Worker>();

    /**
     * Wait condition to support awaitTermination
     */
    private final Condition termination = mainLock.newCondition();

    /**
     * Tracks largest attained pool size. Accessed only under
     * mainLock.
     */
    private int largestPoolSize;

    /**
     * Counter for completed tasks. Updated only on termination of
     * worker threads. Accessed only under mainLock.
     */
    private long completedTaskCount;

